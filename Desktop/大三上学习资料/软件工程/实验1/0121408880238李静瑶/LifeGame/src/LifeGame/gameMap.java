private void setShape(int[][] shape)
{
	changingFlag=true;
	int shapeRow=shape.length;
	int shapeCol=shape[1].length;
	
	int minRow=(shapeRow<row)?shapeRow:row;
	int minCol=(shapeCol<col)?shapeCol:col;
	
	synchronized(this){
		for(int i=0;i<row;i++)
		{
			for(int j=0;j<col;j++)
				currentGeneration[i][j] = cellStatus.Dead;
		}
		
		for(int i=0;i<minRow;i++)
		{
			for(int j=0;j<minCol;j++)
				if(shape[i][j]==1)
					currentGeneration[i][j]=cellStatus.Active;
			
		}
		changingFlag=false;
		this.notifyAll(); //this thread end
	}
}

